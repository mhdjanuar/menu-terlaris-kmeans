    public void getAllDataSales() {
        // Ambil data sales dari database
        List<SalesModel> salesList = salesDao.findAll();

        // Set Model untuk JTable
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new Object[]{
            "ID Sales", "Menu (Tipe)", "Bulan - Tahun", "Total Terjual"
        });

        // Masukkan data sales ke dalam model JTable
        for (SalesModel sales : salesList) {
            model.addRow(new Object[]{
                sales.getIdSales(),
                sales.getNamaMenu() + " - " + sales.getTipeMenu(), // gabung nama + tipe
                sales.getBulanTahun(),
                sales.getTotalTerjual()
            });
        }

        // Set model ke JTable
        jTable1.setModel(model);
    }
 
    public void loadMenuToComboBox() {
        List<MenuMakananModel> menuList = menuMakananDao.findAll();
        jComboBox1.removeAllItems();
        menuIdList.clear(); // penting

        for (MenuMakananModel menu : menuList) {
            String label = menu.getNamaMenu() + " - " + menu.getTipeMenu();

            jComboBox1.addItem(label);

            // Simpan ID sesuai urutan item combobox
            menuIdList.add(menu.getIdMenu());
        }
    }
    
    public void loadBulanToComboBox() {
        String[] bulan = {
            "Januari", "Februari", "Maret", "April", "Mei", "Juni",
            "Juli", "Agustus", "September", "Oktober", "November", "Desember"
        };

        jComboBox2.removeAllItems();

        for (String b : bulan) {
            jComboBox2.addItem(b);
        }
    }
  
