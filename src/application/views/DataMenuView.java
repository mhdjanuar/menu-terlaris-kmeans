    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // Ambil nilai input
        String nama = jTextField1.getText().trim();
        String kategori = jComboBox1.getSelectedItem().toString();
        String subKategori = jComboBox2.getSelectedItem().toString();
        String tipeMenu = jComboBox3.getSelectedItem().toString();
        String hargaStr = jTextField2.getText().trim();

        // Validasi sederhana
        if (nama.isEmpty() || hargaStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama dan harga harus diisi!");
            return;
        }

        int harga;
        try {
            harga = Integer.parseInt(hargaStr);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Harga harus berupa angka!");
            return;
        }

        // Set ke model
        MenuMakananModel menu = new MenuMakananModel();
        menu.setNamaMenu(nama);
        menu.setKategori(kategori);
        menu.setSubkategori(subKategori);
        menu.setTipeMenu(tipeMenu);
        menu.setHarga(harga);

        // Simpan DB via DAO
        int result = menuMakananDao.create(menu);
        if (result > 0) {
            JOptionPane.showMessageDialog(this, "Data menu makanan berhasil disimpan.");
            getAllData(); // Reload data JTable
            clearForm();     // Kosongkan input form
        } else {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan data menu makanan.");
        }
    }//GEN-LAST:event_jButton1MouseClicked

    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        // TODO add your handling code here:
         int selectedRow = jTable1.getSelectedRow();

        // Pastikan baris tabel dipilih
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data dari tabel terlebih dahulu!");
            return;
        }

        // Ambil ID langsung dari tabel (kolom 0)
        int id = Integer.parseInt(jTable1.getValueAt(selectedRow, 0).toString());

        // Ambil input dari form
        String nama = jTextField1.getText().trim();
        String kategori = jComboBox1.getSelectedItem().toString();
        String subKategori = jComboBox2.getSelectedItem().toString();
        String tipeMenu = jComboBox3.getSelectedItem().toString();
        int harga = Integer.parseInt(jTextField2.getText().trim());

        // Set ke model
        MenuMakananModel menu = new MenuMakananModel();
        menu.setIdMenu(id);
        menu.setNamaMenu(nama);
        menu.setKategori(kategori);
        menu.setSubkategori(subKategori);
        menu.setTipeMenu(tipeMenu);
        menu.setHarga(harga);

        // Update ke DB
        int result = menuMakananDao.update(menu);
        if (result > 0) {
            JOptionPane.showMessageDialog(this, "Data menu berhasil diperbarui.");
            getAllData();
            clearForm();
        } else {
            JOptionPane.showMessageDialog(this, "Gagal memperbarui data menu.");
        }
    }//GEN-LAST:event_jButton2MouseClicked

    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        // TODO add your handling code here:
       
       int selectedRow = jTable1.getSelectedRow();

        // Pastikan baris tabel dipilih
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data dari tabel terlebih dahulu!");
            return;
        }
        
        int konfirmasi = JOptionPane.showConfirmDialog(null, "Yakin ingin menghapus data ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if (konfirmasi == JOptionPane.YES_OPTION) {
            int id = Integer.parseInt(jTable1.getValueAt(selectedRow, 0).toString());
            
            int success = menuMakananDao.delete(id);
            if (success > 0) {
                JOptionPane.showMessageDialog(null, "Data berhasil dihapus.");
                getAllData(); // reload data tabel
                clearForm();
            } else {
                JOptionPane.showMessageDialog(null, "Gagal menghapus data.");
            }
        }
    }//GEN-LAST:event_jButton3MouseClicked


