    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data yang akan dihapus!");
            return;
        }

        // Ambil ID Sales dari kolom 0 JTable
        int idSales = (int) jTable1.getValueAt(selectedRow, 0);

        // Konfirmasi sebelum delete
        int confirm = JOptionPane.showConfirmDialog(
                this, 
                "Apakah Anda yakin ingin menghapus data ini?", 
                "Konfirmasi Delete", 
                JOptionPane.YES_NO_OPTION
        );

        if (confirm == JOptionPane.YES_OPTION) {
            int result = salesDao.delete(idSales);
            if (result > 0) {
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
                getAllDataSales(); // refresh JTable
            } else {
                JOptionPane.showMessageDialog(this, "Delete gagal!");
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         try {
            // Ambil ID menu dari menuIdList
            int selectedIndex = jComboBox1.getSelectedIndex();
            if (selectedIndex == -1) {
                JOptionPane.showMessageDialog(this, "Silakan pilih menu.");
                return;
            }
            int idMenu = menuIdList.get(selectedIndex);

            // Ambil bulan (1â€“12)
            int bulan = jComboBox2.getSelectedIndex() + 1;

            // Ambil tahun
            int tahun = Integer.parseInt(jTextField1.getText());

            // Ambil total terjual
            int totalTerjual = Integer.parseInt(jTextField2.getText());

            // Buat model sales
            SalesModel sales = new SalesModel();
            sales.setIdMenu(idMenu);
            sales.setBulan(bulan);
            sales.setTahun(tahun);
            sales.setTotalTerjual(totalTerjual);

            // Simpan ke database
            int insertedId = salesDao.create(sales);

            JOptionPane.showMessageDialog(this, "Data berhasil disimpan! ID: " + insertedId);

            // Refresh tabel
            getAllDataSales();

        } catch (NumberFormatException nfe) {
            JOptionPane.showMessageDialog(this, "Tahun atau total terjual harus berupa angka.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan data: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        // Ambil ID Sales dari JTable
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data yang akan diupdate!");
            return;
        }

        int idSales = (int) jTable1.getValueAt(selectedRow, 0); // kolom 0 = ID Sales

        // Ambil id_menu sesuai combobox (menuIdList sudah disiapkan)
        int menuIndex = jComboBox1.getSelectedIndex();
        if (menuIndex < 0) {
            JOptionPane.showMessageDialog(this, "Pilih menu!");
            return;
        }
        int idMenu = menuIdList.get(menuIndex);

        // Ambil bulan dan tahun dari combobox/textfield
        int bulan = jComboBox2.getSelectedIndex() + 1; // bulan 1-12
        int tahun;
        try {
            tahun = Integer.parseInt(jTextField1.getText());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Tahun tidak valid!");
            return;
        }

        // Ambil total terjual dari textfield
        int totalTerjual;
        try {
            totalTerjual = Integer.parseInt(jTextField2.getText());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Total Terjual tidak valid!");
            return;
        }

        // Buat objek SalesModel
        SalesModel sales = new SalesModel();
        sales.setIdSales(idSales);
        sales.setIdMenu(idMenu);
        sales.setBulan(bulan);
        sales.setTahun(tahun);
        sales.setTotalTerjual(totalTerjual);

        // Panggil update di DAO
        int result = salesDao.update(sales);
        if (result > 0) {
            JOptionPane.showMessageDialog(this, "Data berhasil diupdate!");
            getAllDataSales(); // refresh JTable
        } else {
            JOptionPane.showMessageDialog(this, "Update gagal!");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
         try {
            String templateName = "LaporanPenjualan.jrxml";
            InputStream reportStream = ReportView.class.getResourceAsStream("/resources/reports/" + templateName);
            JasperDesign jd = JRXmlLoader.load(reportStream);

            Connection dbConnection = DatabaseUtil.getInstance().getConnection();

            JasperReport jr = JasperCompileManager.compileReport(jd);

            HashMap parameter = new HashMap();
            parameter.put("PATH","src/resources/images/");
            
            JasperPrint jp = JasperFillManager.fillReport(jr,parameter, dbConnection);
            JasperViewer.viewReport(jp, false);
        } catch (JRException ex) {
            Logger.getLogger(ReportView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton4ActionPerformed


