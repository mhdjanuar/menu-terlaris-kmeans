    public void getAllData() {
            // Ambil data karyawan dari database
            List<MenuMakananModel> menuMakananList = menuMakananDao.findAll();

            // Set Model untuk JTable
            DefaultTableModel model = new DefaultTableModel();
            model.setColumnIdentifiers(new Object[]{
                "ID Menu", "Nama", "Kategori", "Sub Kategori", "Tipe Menu", "Harga",
            });

            // Masukkan data karyawan ke dalam model JTable
            for (MenuMakananModel menuMakanan : menuMakananList) {
                model.addRow(new Object[]{
                    menuMakanan.getIdMenu(),
                    menuMakanan.getNamaMenu(),
                    menuMakanan.getKategori(),
                    menuMakanan.getSubkategori(),
                    menuMakanan.getTipeMenu(),
                    menuMakanan.getHarga()
                });
            }

            // Set model ke JTable
            jTable1.setModel(model);
        }
    
    
    
    
